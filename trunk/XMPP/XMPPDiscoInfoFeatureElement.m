#import "XMPPDiscoInfoFeatureElement.h"

@implementation XMPPDiscoInfoFeatureElement

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark Constructors/Destructors
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithName:(NSString *)name
{
	self = [super initWithXMLElement:[NSXMLElement elementWithName:@"feature"]];
	if (self != nil)
	{
		self.name = name;
	}
	return self;
}	

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark Accessors
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (NSString *)name
{
	return [[self.xmlElement attributeForName:@"var"] stringValue];
}

- (void)setName:(NSString *)aName
{
	[self.xmlElement setStringValue:aName forAttributeWithName:@"var"];
}
@end
